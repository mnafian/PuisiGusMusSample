public class PuisiItem {
    private String mPengarang;
    private String mJudulPuisi;
    private String linkPuisiDownload;
    private String mLinkImage;
    private String mPuisi;

    public String getmPengarang() {
        return mPengarang;
    }

    public void setmPengarang(String mPengarang) {
        this.mPengarang = mPengarang;
    }

    public String getmJudulPuisi() {
        return mJudulPuisi;
    }

    public void setmJudulPuisi(String mJudulPuisi) {
        this.mJudulPuisi = mJudulPuisi;
    }

    public String getLinkPuisiDownload() {
        return linkPuisiDownload;
    }

    public void setLinkPuisiDownload(String linkPuisiDownload) {
        this.linkPuisiDownload = linkPuisiDownload;
    }

    public String getmLinkImage() {
        return mLinkImage;
    }

    public void setmLinkImage(String mLinkImage) {
        this.mLinkImage = mLinkImage;
    }

    public String getmPuisi() {
        return mPuisi;
    }

    public void setmPuisi(String mPuisi) {
        this.mPuisi = mPuisi;
    }
}
